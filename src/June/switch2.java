package June;

public class switch2 {
    public static void main(String[] args) {
        String tab = "army";
        switch (tab){
            case "military":
                System.out.println("military");
                break;
                case "civil":
                System.out.println("civil");
                break;
            default:
                System.out.println("default");
        }
    }
}
